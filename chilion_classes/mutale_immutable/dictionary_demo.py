# what is dictionary?
# it is a information store where all the data is saved by a unique object.

# unlike lists and tuple in dictionary we have keys at the place of indexes.

first_dict = {"Chilion": 17, "apoorva": 13, "divya": 10, "Nagesh": 17, "Ben": 14, "Joy": 11}


# this is how you access any data in the dictionary
mark1 = first_dict["Ben"] 
print(mark1)

# now we will see how to change value of any key
# changing the value of any key
first_dict["divya"] = 12

print(first_dict)