
# --------------
# Integer Datatype
# --------------
print("Working on # Integer #")
num1 = 14
num2 = 5
print(num1 * num2)  # multiplication
print(num1 / num2)
print(num1 // num2)  # floor division
print(num1 % num2)
print(num1 ** num2)
print(num1 + num2)
print(num1 - num2)


# -----------------------
# Floating point Datatype
# -----------------------

print("Working on # Floating Point #")
num1 = 14.89
num2 = 5.87
print(num1 * num2)  # multiplication
print(num1 / num2)
print(num1 // num2)  # floor division
print(num1 % num2)
print(num1 ** num2)
print(num1 + num2)
print(num1 - num2)

